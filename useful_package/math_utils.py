def add(a: int, b: int) -> int:
    """Add two integers.

    Args:
        a (int): integer a
        b (int): integer b

    Returns:
        int: result of addition.
    """
    return a + b


def subtract(a: int, b: int) -> int:
    """Subtract an integer from another.

    Args:
        a (int): integer a
        b (int): integer b

    Returns:
        int: result of subtraction.
    """
    return a - b
